// Μετατρέπει ονόματα σε "ασφαλή" filenames (λατινικοί χαρακτήρες, παύλες)
export function slugifyGreek(input: string) {
  const map: Record<string,string> = {
    'ά':'a','έ':'e','ή':'i','ί':'i','ό':'o','ύ':'y','ώ':'o','ϊ':'i','ΐ':'i','ϋ':'y','ΰ':'y',
    'Α':'a','Β':'v','Γ':'g','Δ':'d','Ε':'e','Ζ':'z','Η':'i','Θ':'th','Ι':'i','Κ':'k','Λ':'l','Μ':'m','Ν':'n','Ξ':'x','Ο':'o','Π':'p','Ρ':'r','Σ':'s','Τ':'t','Υ':'y','Φ':'f','Χ':'x','Ψ':'ps','Ω':'o',
    'α':'a','β':'v','γ':'g','δ':'d','ε':'e','ζ':'z','η':'i','θ':'th','ι':'i','κ':'k','λ':'l','μ':'m','ν':'n','ξ':'x','ο':'o','π':'p','ρ':'r','σ':'s','ς':'s','τ':'t','υ':'y','φ':'f','χ':'x','ψ':'ps','ω':'o',
  };
  const noAccents = input.split('').map(ch => map[ch] ?? ch).join('');
  return noAccents
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)+/g, '');
}
